#Se puede utiliar las funciones dir y help para consultar el uso de los metodos de un tipo de dato

dir(str) #Retorna el listado de metodos para str
help(str.upper) #Retorna el uso del metodo upper

#Para aplicar un metodo:

x = "hello".upper() #un metodo necesita el "dot notation"
y = "hello"
z = y.upper()

print(x,z)



